import { type NextPage } from "next";
import Head from "next/head";
import Link from "next/link";
import { useUser } from "@clerk/nextjs";

import { api } from "~/utils/api";

const Home: NextPage = () => {
  const hello = api.posts.hello.useQuery({ text: "Niggers" });
  const all = api.posts.getAll.useQuery();
  const user = useUser();

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center bg-gradient-to-b from-[#2e026d] to-[#15162c] text-white">
        {user.isSignedIn ? (
          <p>hi {user.user.username} </p>
        ) : (
          <p>{hello.data?.greeting}</p>
        )}
        {!user.isSignedIn && <p>please signin or singup</p>}
        <h1 className="text-xl">All Posts:</h1>
        {all.data?.map((item) => {
          return (
            <div key={item.id}>
              <p>{item.id}</p>
              <p>{item.authorID}</p>
              <p>{item.content}</p>
              <p>{item.createdAt.toString()}</p>
            </div>
          );
        })}
      </main>
    </>
  );
};

export default Home;
